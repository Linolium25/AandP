#include <iostream> 
#include <cstdlib>    // для функції rand()
#include <ctime>      // для ініціалізації генератора випадкових чисел
using namespace std;

int main() {
    int n, p;  // n — кількість рядків, p — кількість стовпців

    // розмір матриці
    cout << "Vvedit kilkist ryadkiv (n): ";
    cin >> n;
    cout << "Vvedit kilkist stovptsiv (p): ";
    cin >> p;

    int A[n][p];  // оголошення двовимірного масиву (матриці) розміром n x p

    srand(time(0));  // ініціалізація генератора випадкових чисел на основі поточного часу

    // Заповнення матриці випадковими числами та виведення її на екран
    cout << "\nAvtomatychno zgenerovana matrytsia:" << endl;
    for (int i = 0; i < n; i++) {           // проходимо кожен рядок
        for (int j = 0; j < p; j++) {       // проходимо кожен стовпець
            *(*(A + i) + j) = rand() % 100; // присвоюємо випадкове число від 0 до 99 через вказівники
            cout << *(*(A + i) + j) << "\t"; // виводимо елемент з табуляцією
        }
        cout << endl; // перехід на новий рядок після кожного рядка матриці
    }

    int minIndex[n]; // одновимірний масив для збереження індексів мінімальних елементів у кожному рядку

    // Пошук індексів мінімальних елементів
    for (int i = 0; i < n; i++) {
        int min = *(*(A + i));  // припускаємо, що перший елемент — мінімальний
        int index = 0;          // індекс мінімального елемента (початково — 0)
        for (int j = 1; j < p; j++) {             // перевіряємо всі інші елементи рядка
            if (*(*(A + i) + j) < min) {          // якщо знайшли менше значення
                min = *(*(A + i) + j);            // оновлюємо мінімум
                index = j;                        // запам’ятовуємо індекс
            }
        }
        minIndex[i] = index; // зберігаємо індекс мінімального елемента в рядку
    }

    // Виведення індексів мінімальних елементів для кожного рядка
    cout << "\n\"Indeksy minimalnykh elementiv u kozhnomu ryadku\":" << endl;
    for (int i = 0; i < n; i++) {
        cout << "\"" << minIndex[i] << "\" "; // виводимо індекси в лапках (без апострофів)
    }

    return 0; // завершення програми
}

